/**
 * Apply the selected language to the entire website
 * @param {string} lang - The language code ('en' or 'ar')
 */
function applyLanguage(lang) {
    // Update HTML dir attribute for RTL support
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    
    // Update current language in localStorage
    localStorage.setItem('language', lang);
    currentLanguage = lang;
    
    // Update language toggle state
    const langToggle = document.getElementById('language-toggle');
    if (langToggle) {
        if (lang === 'ar') {
            langToggle.classList.add('ar-active');
        } else {
            langToggle.classList.remove('ar-active');
        }
        
        // Update active class on language options
        document.querySelectorAll('.lang-option').forEach(option => {
            if (option.getAttribute('data-lang') === lang) {
                option.classList.add('active');
            } else {
                option.classList.remove('active');
            }
        });
    }
    
    // Translate all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[key] && translations[key][lang]) {
            element.innerHTML = translations[key][lang];
        }
    });
    
    // Update form placeholders
    updateFormPlaceholders(lang);
    
    // Trigger a resize event to fix any layout issues
    window.dispatchEvent(new Event('resize'));
}

/**
 * Set up language toggle event listeners
 */
function setupLanguageToggle() {
    const langToggle = document.getElementById('language-toggle');
    if (langToggle) {
        const langOptions = langToggle.querySelectorAll('.lang-option');
        
        langOptions.forEach(option => {
            option.addEventListener('click', function() {
                const lang = this.getAttribute('data-lang');
                applyLanguage(lang);
            });
            
            // Set initial active state
            if (option.getAttribute('data-lang') === currentLanguage) {
                option.classList.add('active');
            }
        });
        
        // Set initial toggle state
        if (currentLanguage === 'ar') {
            langToggle.classList.add('ar-active');
        }
    }
}

/**
 * Update form placeholders with translated text
 * @param {string} lang - The language code
 */
function updateFormPlaceholders(lang) {
    // Update contact form placeholders
    const nameInput = document.querySelector('input[name="name"]');
    const emailInput = document.querySelector('input[name="email"]');
    const messageTextarea = document.querySelector('textarea[name="message"]');
    
    if (nameInput && translations["contact-name"][lang]) {
        nameInput.placeholder = translations["contact-name"][lang];
    }
    
    if (emailInput && translations["contact-email"][lang]) {
        emailInput.placeholder = translations["contact-email"][lang];
    }
    
    if (messageTextarea && translations["contact-message"][lang]) {
        messageTextarea.placeholder = translations["contact-message"][lang];
    }
} 